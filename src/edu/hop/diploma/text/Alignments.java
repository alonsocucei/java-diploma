package edu.hop.diploma.text;

/**
 * Tipo enumerado que contiene las constantes para las diferentes alineaciones
 * posibles para un texto.
 * @author jjsanche
 */
public enum Alignments {
    /**
     * Constante que representa la alineación izquierda.
     */
    LEFT,
    /**
     * Constante que representa la alineación centrada.
     */
    CENTER,
    /**
     * Constante que representa la alineación derecha.
     */
    RIGHT
}
