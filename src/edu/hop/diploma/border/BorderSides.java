package edu.hop.diploma.border;

/**
 * Contiene los 4 lados donde se puede aplicar un borde para un documento.
 * 
 * @author jjsanche
 */
public enum BorderSides {
    /**
     * Constante que representa el lado izquierdo de un documento.
     */
    LEFT,
    /**
     * Constante que representa el lado derecho de un documento.
     */
    RIGHT, 
    /**
     * Constante que representa la parte superior de un documento.
     */
    TOP,
    /**
     * Constante que representa la parte inferior de un documento.
     */
    BOTTOM
}
